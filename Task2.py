import math
def sqrt(n): return math.sqrt(n)
def log(n): return math.log(n)
def sin(n): return math.sin(n)

x=float(input("Enter any number: ")) 
print ("Square root of",x,"is:",sqrt(x)) 
print ("Log of",x,"is:",log(x)) 
print ("sin of",x,"is:",sin(x))
